<p>BTEC is a leading teacher training (12+4) institution under the Ministry of Edcuation, Youth & Sport (MoEYS) Cambodia. The institute was established in 2018 with the primary vision to train the 21st century teachers to enhance the quality of education in Cambodia, particularly at primary and lower secondary education. To achieve this vision, BTEC has developed the five-year strategic plan 2024-2028 based on the three main missions: (i) to train the highly qualified student teachers with 21th century skills, (ii) to improve the research quality and capacity in teacher training centers and (iii) to provide social services to support the educational reforms and as well the Pentagonal Strategy (Phase 1: 2023 -2028) of the Royal Government of Cambodia.</p>
<p>The core concepts of BTEC training program focus on the Quality of educational program and the Excellence of our master teachers and student teachers, which we believe to build up our “Branding” toward the international standard teacher training hub in Cambodia and ASEAN region. Therefore, all teachers at primary and lower secondary schools must be trained and equipped by the technological pedagogical content knowledge (TPAC) including other necessary 21st century skills such as 4Cs skills (critical thinking, creativity, collaboration, communication), digital literacy, languages and ICT skills, while improving the teaching & research capacity of our master teachers at the Regional Teacher Training Center (PTTC) and Provincial Teacher Training School (PTTS) in northwestern Cambodia.</p>
<p>BTEC is working very closely with all development partners (local government, NGOs and private sectors) to promote inclusive education and home internationalization of our education programs. Furthermore, we are keen to promote STEM education, research-development & innovation (R/D & I) ecosystem to improve our research capability and capacity in response to the local and regional needs in term of education services and challenges. Recently, we have expanded our international network to ASEAN region and Europe to enhance our digital education policy, particularly in pedagogical reforms. This will support our RGC’s digital economy and society policies, which are the foundation to fulfill the vision of RGC to be the Upper Middle Income Country by 2030 and High income country by 2050.</p>
