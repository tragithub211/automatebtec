                        <p>សិក្ខាកាមទាំងអស់សម្ដែងនូវអារម្មណ៍រីករាយដែលបានចុះឈ្មោះចូលរៀនវគ្គសិក្សាខ្លីខាងលើ និងបន្តគាំទ្រវិទ្យាស្ថានលើវគ្គសិក្សាផ្សេងទៀត ដើម្បីចូលរួមចំណែកក្នុងការពង្រឹងវិធីសាស្រ្តបង្រៀន និងអនុវត្តវិធីសាស្រ្តបង្រៀនបែបថ្មីដោយឈរលើស្មារតីពង្រឹង ៨០ ពង្រីក ២០ របស់រាជរដ្ឋាភិបាលកម្ពុជាក្នុងអាណត្តិថ្មីនេះ។</p>

