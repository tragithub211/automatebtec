                        <h6 style="margin-top:50px;">English below:</h6>
                        <p>On Monday, November 11, 2024, Global Wing youth members from the Fukuoka and Kizuna organizations in Japan visited Battambang Teacher Education College. The visit aimed to foster cultural exchange and strengthen friendship between the institutions.</p>
                        <p>His Excellency Dr. Chea Ratha, Director of Battambang Teacher Education College, welcomed the delegation with remarks and presented souvenirs. The visitors then inspected and activated a smart television—a valuable gift from the Fukuoka and Kizuna organizations. The event featured cultural performances and recreational games jointly presented by the Global Wing youth group and the college's student teachers.</p>
                        <p>The visit concluded with a joyful shared meal, celebrating in an atmosphere of harmony, unity, and friendship.</p>
