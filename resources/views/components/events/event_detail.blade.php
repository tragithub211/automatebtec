<h4 class="it-evn-details-title">Project-Based Learning: A Six-Steps Approach to Creating a PBL Project</h4>
<div class="postbox__meta">
<span><i class="fa-light fa-file-invoice"></i>Session 1</span>
<span><i class="fa-light fa-clock"></i>02:00 PM - 05.00 PM</span>
<span><i class="fa-light fa-location-dot"></i>BTEC - Confference Room 1</span>
</div>
<div class="it-evn-details-text mb-40">
<p>The landscape of higher education is evolving rapidly, and educators are continually seeking innovative approaches to engage students and foster deep learning. Project-Based Learning (PBL) has emerged as a pedagogical strategy that not only enhances students' critical thinking skills but also promotes collaboration and real-world application of knowledge. To equip university lecturers with the necessary tools and methodologies to implement PBL effectively, we propose a comprehensive workshop that will provide a hands-on experience and valuable insights into integrating PBL into various disciplines.</p>
</div>
   <div class="it-evn-details-text">
      <h6 class="it-evn-details-title-sm pb-10">Objectives</h6>
      <p>The primary objectives of the workshop are as follows:</p>
      <p>1. To introduce university lecturers to the principles and benefits of Project-Based Learning.</p>
      <p>2. To provide participants with practical strategies for designing, implementing, and assessing PBL activities.</p>
      <p>3. To facilitate collaboration and knowledge-sharing among participants for the development of discipline specific PBL modules.</p>
      <p>4. To address challenges and concerns related to PBL implementation and suggest effective solutions.</p>
   </div>
    <div class="it-evn-details-text">
        <h6 class="it-evn-details-title-sm pb-10">Workshop Structure</h6>
        <p>The workshop will be conducted over a two-day period and will include a combination of lectures, hands-on activities, group discussions, and collaborative sessions. The sessions will cover the following key areas:</p>
        <p><strong>1. Introduction to Project-Based Learning</strong></p>
        <ul class="pl-50">
            <li>Definition and principles of PBL</li>
            <li>Benefits of PBL in higher education</li>
            <li>Successful examples of PBL implementation across disciplines</li>
        </ul>
        <p><strong>2. Designing PBL Modules</strong></p>
        <ul class="pl-50">
            <li>Identifying learning outcomes and objectives</li>
            <li>Developing authentic and challenging projects</li>
            <li>Aligning projects with course content and curriculum</li>
        </ul>

        <p><strong>3. Implementation Strategies</strong></p>
        <ul class="pl-50">
            <li>Facilitating student engagement and collaboration</li>
            <li>Managing student groups and dynamics</li>
            <li>Integrating technology into PBL activities</li>
        </ul>

        <p><strong>4. Assessment and Feedback in PBL</strong></p>
        <ul class="pl-50">
            <li>Formative and summative assessment methods</li>
            <li>Providing constructive feedback</li>
            <li>Evaluating individual and group performance</li>
        </ul>

        <p><strong>5. Overcoming Challenges in PBL</strong></p>
        <ul class="pl-50">
            <li>Addressing resistance and skepticism</li>
            <li>Time management and logistical considerations</li>
            <li>Adapting PBL to different learning environments</li>
        </ul>

        <p><strong>6. Collaborative Module Development</strong></p>
        <ul class="pl-50">
            <li>Small group activities for developing PBL modules</li>
            <li>Peer review and feedback on module designs</li>
            <li>Sharing best practices and ideas</li>
        </ul>
    </div>
