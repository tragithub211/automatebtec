                        <p>នារសៀលថ្ងៃព្រហស្បតិ៍ ទី២៧ ខែមិថុនា ឆ្នាំ២០២៤</p>
                        <p>មហាវិទ្យាល័យស្រាវជ្រាវគុរកោសល្យនៃវិទ្យាស្ថានគរុកោសល្យបាត់ដំបងបានរៀបចំកម្មវិធីសិក្ខាសាលាស្តីពី ការសរសេរសំណើគម្រោងស្រាវជ្រាវ ដល់គរុនិស្សិតនៃវិទ្យាស្ថាន ក្រោមអធិបតីភាព ឯកឧត្តមបណ្ឌិត ជា រដ្ឋា នាយកវិទ្យាស្ថានគរុកោសល្យបាត់ដំបង ដែលសិក្ខាសាលានេះមានគោលបំណង៖</p>
                        <p>(១)បញ្ជ្រាបការយល់ដឹងពីទម្រង់នៃសំណើគម្រោងស្រាវជ្រាវប្រតិបត្តិ(Action research) និង</p>
                        <p>(២)ណែនាំលើរបៀបនៃការសរសេរសំណើគម្រោងស្រាវជ្រាវសម្រាប់ជំពូកទី១ ដល់ជំពូកទី៣។ ឯកឧត្តមបណ្ឌិត នាយកវិទ្យាស្ថាន បានចែករំលែកនូវបទពិសោធន៍ស្រាវជ្រាវល្អៗ ព្រមទាំងបានវាយតម្លៃខ្ពស់ចំពោះការរៀបចំកម្មវិធីសិក្ខាសាលារបស់មហាវិទ្យាល័យស្រាវជ្រាវគរុកោសល្យ និងផ្តល់អនុសាសន៍មួយចំនួនដូចខាងក្រោម៖</p>

