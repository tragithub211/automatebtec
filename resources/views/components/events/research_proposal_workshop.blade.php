                            <p>(២) គរុនិស្សិតទាំងអស់ត្រូវកំណត់បញ្ហាប្រឈមលើការរៀន និងបង្រៀន ផ្សាភ្ជាប់សំណួរស្រាវជ្រាវទៅនឹងបញ្ហាប្រឈមក្នុងថ្នាក់រៀន ដើម្បីធ្វើឱ្យប្រសើរឡើងលើលទ្ធផលបង្រៀននិងរៀនរបស់សិស្</p>
                            <p>(៣) ជំរុញគរុនិស្សិតក្នុងការសរសេររបាយការណ៍ស្រាវជ្រាវប្រកបដោយគុណភាពដើម្បីបន្សល់ទុកជាឯកសារសម្រាប់អ្នកសិក្សាស្រាវជ្រាវជំនាន់ក្រោយ ពិសេសឯកសារជាភាសាខ្មែរ</p>
                            <p>(៤) ជំរុញគ្រូឧទ្ទេសគាំទ្រដល់ការងារស្រាវជ្រាវរបស់ប្អូនៗគរុនិស្សិតឆ្លើយតបនឹងស្តង់ដាវិជ្ជាជីវៈគរុនិស្សិត និងស្តង់ដាវិជ្ជាជីវៈគ្រូឧទ្ទេស</p>
                            <p>(៥) វិទ្យាស្ថាននឹងផ្តល់ការលើកទឹកចិត្តសម្រាប់គរុនិស្សិតដែលមានស្នាដៃស្រាវជ្រាវល្អ និងជំរុញឱ្យគរុនិស្សិតចូលរួមធ្វើបទបង្ហាញលទ្ធផលស្រាវជ្រាវក្នុងសន្និសីទអប់រំរបស់វិទ្យាស្ថាននៅពេលខាងមុខ និងបោះពុម្ពផ្សាយលទ្ធផលស្រាវជ្រាវក្នុងទស្សាវដ្តីស្រាវជ្រាវអប់រំថ្នាក់ជាតិ និងអន្តរជាតិ</p>
