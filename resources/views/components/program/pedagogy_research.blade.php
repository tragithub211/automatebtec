<h3>Acting Dean’s Message</h3>
<p>On behalf of Faculty of Pedagogy Research of Battambang Teacher Education College (BTEC), I would like to urge everyone to visit our website to find out more about the faculty's current courses and research initiatives, as well as other aspects of psychology, pedagogy, research, and library in advancing the pre-service teachers’ personal and professional growth.</p>
<p>This website is intended to provide information and communication for anyone interested in our faculty, whether they are student teachers tracking a bachelor's degree (or bachelor's degree + 1)  in teaching, graduates looking to further their education, or in-service teachers seeking continuous professional development (CPD) to improve teaching and researching.</p>
<p>The Faculty of Pedagogy Research presently oversees two departments: the Pedagogy Department and the Educational Research and Library Department. Our goal is to provide our student teachers with an appropriately broad teacher education program, with a focus on continuous improvement of teaching quality, applied research, and services, as well as adherence to the criteria established for courses of study offered within the framework of teacher education pedagogy.</p>
<p>Educational research is an essential means for developing and increasing the scientific impact of new knowledge, as well as improving instruction, and it is critical to maintaining our teaching program's lead in this area. To accomplish this, we work closely with other institutions and maintain strong relationships with a varied network of social and professional partners, with the goal of establishing beneficial collaboration in all areas of teacher education, educational research, educational psychology, and innovative pedagogy.</p>
<p>We urge all of our visitors to explore the numerous possibilities that the website provides, and we look forward to hearing from you via any of the channels available to you.</p>
<p class="pt-20">Kind regards, </p>
<p>SON Sokun</p>
